const languageSelect = document.getElementById("languageSelect");
const calendarTitle = document.getElementById("calendarTitle");
const weekdaysContainer = document.getElementById("weekdays");
const calendarGrid = document.getElementById("calendarGrid");
const modal = document.getElementById("reservationModal");
const selectedDateLabel = document.getElementById("selectedDateLabel");
const emailReservationLink = document.getElementById("emailReservationLink");

let currentLang = "pt";
let currentDate = new Date();
let selectedDate = null;

const normalizeWeekDayOffset = (date) => {
  const day = date.getDay();
  return day === 0 ? 6 : day - 1;
};

const formatDate = (date) => {
  return new Intl.DateTimeFormat(currentLang, {
    day: "2-digit",
    month: "long",
    year: "numeric"
  }).format(date);
};

const setLanguage = (lang) => {
  currentLang = lang;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    const value = i18n[lang][key];
    if (typeof value === "string") {
      if (key === "footer") {
        const year = new Date().getFullYear();
        el.innerHTML = value.replace("{{year}}", year);
      } else {
        el.textContent = value;
      }
    }
  });

  renderWeekdays();
  renderCalendar(currentDate.getFullYear(), currentDate.getMonth());

  if (selectedDate) {
    const formatted = formatDate(selectedDate);
    selectedDateLabel.textContent = i18n[currentLang].selectedDateLabel.replace("{{date}}", formatted);
    updateEmailLink();
  }
};

const renderWeekdays = () => {
  weekdaysContainer.innerHTML = "";
  i18n[currentLang].weekdays.forEach((day) => {
    const el = document.createElement("div");
    el.className = "weekday";
    el.textContent = day;
    weekdaysContainer.appendChild(el);
  });
};

const renderCalendar = (year, month) => {
  const months = i18n[currentLang].months;
  calendarTitle.textContent = i18n[currentLang].calendarTitle(months[month], year);
  calendarGrid.innerHTML = "";

  const firstDay = new Date(year, month, 1);
  const lastDay = new Date(year, month + 1, 0);
  const offset = normalizeWeekDayOffset(firstDay);

  for (let i = 0; i < offset; i += 1) {
    const empty = document.createElement("div");
    empty.className = "day-empty";
    calendarGrid.appendChild(empty);
  }

  for (let day = 1; day <= lastDay.getDate(); day += 1) {
    const btn = document.createElement("button");
    btn.className = "day-btn";
    btn.textContent = day;

    const dateObj = new Date(year, month, day);
    btn.addEventListener("click", () => {
      selectedDate = dateObj;
      document.querySelectorAll(".day-btn").forEach((d) => d.classList.remove("selected"));
      btn.classList.add("selected");
      openReservationModal();
    });

    if (
      selectedDate &&
      dateObj.getDate() === selectedDate.getDate() &&
      dateObj.getMonth() === selectedDate.getMonth() &&
      dateObj.getFullYear() === selectedDate.getFullYear()
    ) {
      btn.classList.add("selected");
    }

    calendarGrid.appendChild(btn);
  }
};

const updateEmailLink = () => {
  if (!selectedDate) return;
  const formatted = formatDate(selectedDate);
  const subject = encodeURIComponent(i18n[currentLang].emailSubject);
  const body = i18n[currentLang].emailBody(formatted);
  emailReservationLink.href = `mailto:?subject=${subject}&body=${body}`;
};

const openReservationModal = () => {
  if (!selectedDate) return;
  const formatted = formatDate(selectedDate);
  selectedDateLabel.textContent = i18n[currentLang].selectedDateLabel.replace("{{date}}", formatted);
  updateEmailLink();
  modal.showModal();
};

document.getElementById("prevMonth").addEventListener("click", () => {
  currentDate = new Date(currentDate.getFullYear(), currentDate.getMonth() - 1, 1);
  renderCalendar(currentDate.getFullYear(), currentDate.getMonth());
});

document.getElementById("nextMonth").addEventListener("click", () => {
  currentDate = new Date(currentDate.getFullYear(), currentDate.getMonth() + 1, 1);
  renderCalendar(currentDate.getFullYear(), currentDate.getMonth());
});

document.getElementById("closeModal").addEventListener("click", () => modal.close());

modal.addEventListener("click", (event) => {
  const rect = modal.getBoundingClientRect();
  const inDialog = rect.top <= event.clientY && event.clientY <= rect.top + rect.height && rect.left <= event.clientX && event.clientX <= rect.left + rect.width;
  if (!inDialog) modal.close();
});

languageSelect.addEventListener("change", (event) => {
  setLanguage(event.target.value);
});

const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.2 }
);

document.querySelectorAll(".section-animate").forEach((section) => observer.observe(section));

document.getElementById("currentYear").textContent = new Date().getFullYear();
setLanguage("pt");
